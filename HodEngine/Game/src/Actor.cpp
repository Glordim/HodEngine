		//-----------------------------------------------------------------------------
		//! @brief		
		//-----------------------------------------------------------------------------
		std::vector<Component*> Actor::GetAllComponent() const
		{
			std::vector<Component*> vComponents;

			auto it = _componentMapping.begin();
			auto itEnd = _componentMapping.end();

			while (it != itEnd)
			{
				vComponents.push_back(it->second);

				++it;
			}

			return vComponents;
		}
