#include "Allocator.h"

namespace HOD
{
	namespace PHYSIC
	{
		//-----------------------------------------------------------------------------
		//! @brief		
		//-----------------------------------------------------------------------------
		Allocator::Allocator()
		{
		}

		//-----------------------------------------------------------------------------
		//! @brief		
		//-----------------------------------------------------------------------------
		Allocator::~Allocator()
		{
		}

		//-----------------------------------------------------------------------------
		//! @brief		
		//-----------------------------------------------------------------------------
		void* Allocator::allocate(size_t size, const char* typeName, const char* filename, int line)
		{
			return new char[size];
		}

		//-----------------------------------------------------------------------------
		//! @brief		
		//-----------------------------------------------------------------------------
		void Allocator::deallocate(void* ptr)
		{
			delete[] ptr;
		}
	}
}
